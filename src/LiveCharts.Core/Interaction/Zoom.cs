namespace LiveCharts.Core.Interaction
{
    /// <summary>
    /// The zooming options.
    /// </summary>
    public enum Zooming
    {
        /// <summary>
        /// Disables zooming.
        /// </summary>
        None,
        /// <summary>
        /// Zooms only the X axis.
        /// </summary>
        X,
        /// <summary>
        /// Zooms only the Y axis.
        /// </summary>
        Y,
        /// <summary>
        /// Zooms X and Y axes.
        /// </summary>
        Xy
    }
}
